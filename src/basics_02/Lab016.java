package src.basics_02;

public class Lab016 {
    public static void main(String[] args) {
        // Assignment operator
        // =
        int a = 90;
        System.out.println(a);

        // Arthmatic Operator - Binary - Requires two operators
        // +, _, *, /

        int num1 = 67;
        int num2 = 89;
        System.out.println(num1 + num2);

        String firstname = "Prashant";
        String lastname = "Chauhan";
        System.out.println(firstname + lastname);
        // concatination happens using + operator

        // Unary Operator - Requires one operator

        int a1 = 10; // +10 , + is something not required
        int b= -10;  // - is required

        System.out.println(a1);
        System.out.println(b);
    }
}
