public class Lab039 {
    public static void main(String[] args) {

        // Increment Operator
        // Pre and Post
        int a = 10;
       //  a = a+1;
       //  a +=1; // compound operator
        a++; // Post increment = a = a+1;
        ++a; // Pre Increment = a = a+1;
        System.out.println(a);


    }
}
